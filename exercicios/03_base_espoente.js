/** 03) Crie uma função que recebe dois parâmetros, base e expoente, 
 * e retorne a base elevada ao expoente.
 * */

const baseExpoente = (base, expoente) => {
  console.log(`O resultado de ${base} com expoente ${expoente} = ${Math.pow(base, expoente)}`)
}
baseExpoente(3, 5)
baseExpoente(2, 2)
baseExpoente(5, 5)